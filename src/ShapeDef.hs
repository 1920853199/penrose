propsWithType :: (Autofloat a) =>
    ValueType -> ShapeType -> ShapeDefs a -> [PropID]
propsWithType propType shapeType defs =
    M.keys $ M.filter (\(t, _) -> t == propType) $ snd $ findDef shapeType defs

